function changeSpeed (speed: number, sprite: Sprite) {
    td = ViewAngle
    tdr = td * 3.1416 / 180
    tx = speed * Math.cos(tdr)
    ty = speed * Math.sin(tdr)
    sprite.setVelocity(tx, ty)
}
